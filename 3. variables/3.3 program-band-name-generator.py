# Band Name Generator
# 1. Create a greeting for your program.
# 2. Ask the user for the city that they grew up in and store it in a variable.
# 3. Ask the user for the name of a pet and store it in a variable.
# 4. Combine the name of their city and pet and show them their band name.

print("Welcome to the band name generator project \n")
city = input("Enter the city that you grew up in \n")
name = input("Enter the name of a pet \n")
print("Your band name could be: " + city + " | " + name)

# output
# Enter the city that you grew up in
# Calicut
# Enter the name of a pet
# Gypsy
# Your band name could be: Calicut | Gypsy